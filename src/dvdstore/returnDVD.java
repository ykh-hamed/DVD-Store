    private void ReturnDVDBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReturnDVDBtnActionPerformed
        // TODO add your handling code here:
        Client u = Admin.retrieveClient(Login.loggedUserName);
        ArrayList <Rentals> rentalsList = u.browseRented();
        Rentals r = rentalsList.get(dvdList.getSelectedIndex());
        String dvdName=r.getDVDName();
        DVD rentedDVD = Admin.retrieveDVD(dvdName);
        int penalty = u.returnDVD(rentedDVD.getID());
        JOptionPane.showMessageDialog(null, "Thanks, Your penalty is: "+penalty);
        refresh();
        displayInfo();
    }//GEN-LAST:event_ReturnDVDBtnActionPerformed

