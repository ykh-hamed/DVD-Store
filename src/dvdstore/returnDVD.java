    /**
     * Loads Information Into the jTextArea
     */
    public static void displayInfo()
    {
        if (!dvdList.isSelectionEmpty())
        {
            Client c = Admin.retrieveClient(Login.loggedUserName);
            ArrayList <Rentals> DVDList = c.browseRented();
            Rentals x = DVDList.get(dvdList.getSelectedIndex());
            InfoArea.setText("DVD Title: "+x.getDVDName()+"\nDue Date: "+x.getDueDate()+"\nRental Price: "+x.getRentalPrice());
        }
        else
        {
            InfoArea.setText(null);
        }
    }
    
    /**
     * Rewrites the DVDList JList after returning DVD
     */
    public void refresh()
    {
        Client c = Admin.retrieveClient(Login.loggedUserName);
        ArrayList <Rentals> DVDList = c.browseRented();
        final String [] DVDTitle = new String [DVDList.size()];
        for(int i=0;i<DVDList.size();i++)
        {
            DVDTitle[i]=DVDList.get(i).getDVDName();
        }
        dvdList = new javax.swing.JList(DVDTitle);

        dvdList.setModel(new javax.swing.AbstractListModel() {
            String[] strings = DVDTitle;
            @Override
            public int getSize() { return strings.length; }
            @Override
            public Object getElementAt(int i) { return strings[i]; }
        });
        jScrollPane1.setViewportView(dvdList);
    }
    
